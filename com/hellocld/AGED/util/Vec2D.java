package com.hellocld.AGED.util;

/**
 * A basic 2D Vector class (same as Pair, just labeled more appropriately)
 * @author CLD
 *
 */
public class Vec2D {
	float x;
	float y;
	
	public Vec2D(float x, float y) {
		this.x = x;
		this.y = y;
	}
	
	public float getX() {
		return x;
	}
	
	public float getY() {
		return y;
	}
	
}
